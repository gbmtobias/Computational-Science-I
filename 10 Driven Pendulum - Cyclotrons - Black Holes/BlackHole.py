from scipy.integrate import odeint
    
def func(y, t):
    r = sqrt(y[0]**2+y[1]**2)
    return [
        y[2]-2*(y[0]*y[2]+y[1]*y[3])*y[0]/(r**3),
        y[3]-2*(y[0]*y[2]+y[1]*y[3])*y[1]/(r**3),
        -0.5*((1-2/r)**-2)*2/(r**2)*y[0]/r + (2*(y[0]*y[2]+y[1]*y[3])*y[2]*r**3-((y[0]*y[2]+y[1]*y[3])**2)*3*(r**2)*y[0]/r)/(r**6),
        -0.5*((1-2/r)**-2)*2/(r**2)*y[1]/r + (2*(y[0]*y[2]+y[1]*y[3])*y[3]*r**3-((y[0]*y[2]+y[1]*y[3])**2)*3*(r**2)*y[1]/r)/(r**6)
        ]
    
t = arange(0, 50, 0.01)

y0 = [8, 0, 0, 0.2]

y = odeint(func, y0, t)

for i in range(5000):
    plot(y[i][0], y[i][1], 'r,')